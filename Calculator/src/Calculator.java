// This calculator object use methods to mimic specific actions of a calculator
public class Calculator {
    public Calculator(){
    }

    // this method is to mimic the add function on a calculator.
    public int add(int a, int b) {
        return  a + b;
    }
    public int subtract(int a, int b) { //acts as a subtraction function of a calculator.
        return  a - b;
    }

}
